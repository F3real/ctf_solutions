_start:
	; greet user
	mov		edx, msg1end - msg1	; arg2: message length
	mov		ecx, msg1		; arg1: message
	mov		ebx, 1			; arg0: stdout
	mov		eax, SYS_WRITE
	int		0x80
	
	; read in the input, expect "XXXXXXXX-XXXXXXXX"
	mov     edi, inbuf
	call    input

	; check input format
	mov		esi, inbuf
	cmp		byte [esi+8], '-'
	jne		.fail
	mov		byte [esi+8], 0

	; parse first ctext
	push	v0
	push	inbuf
	call	parse_uint32_hex
	cmp		eax, -1
	je		.fail

	; parse second ctext
	push	v1
	push	inbuf+9
	call	parse_uint32_hex
	cmp		eax, -1
	je		.fail

	; decipher
	push	v0
	call	decipher

	; check
	cld                         ; clear direction flag so that string pointers auto increment after each string 
	mov		esi, v0             ; operation
	lodsd                       ; load string instructions (loads to EAX)
	cmp		eax, 0x57415343		; 'CSAW'
	jne		.fail
	lodsd
	cmp		eax, 0x41484148		; 'HAHA'
	jne		.fail

.pass:
	mov     al, 1
	out     2, al
	mov		edx, msg2end-msg2
	mov		ecx, msg2
	jmp		.done
.fail:
	mov		edx, msg3end-msg3
	mov		ecx, msg3
.done:
	mov		ebx, 1
	mov		eax, SYS_WRITE
	int		0x80

	mov 	ecx, 120
.sleep_loop:
	pause
	loop 	.sleep_loop
	jmp 	_start

parse_nib:
	push	ebp
	mov		ebp,esp
	push	ebx
	mov		eax, dword[ebp+0x8]
	mov		ecx, dword[ebp+0xc]
	mov		dl, byte[eax]
	lea		eax, [edx-0x30]
	cmp		al,0x9
	ja		.not_digit
.digit:
	mov		byte [ecx],al
	jmp		.done_ok
.not_digit:
	lea		eax, [edx-0x61]
	cmp		al, 0x5
	ja		.not_lower
.lower:
	sub		edx, 0x57
	jmp		.write_result
.not_lower:
	lea		ebx,[edx-0x41]
	mov		eax, -1
	cmp		bl, 0x5
	ja		.done
.upper:
	sub		edx, 0x37
.write_result:
	mov		byte [ecx],dl
.done_ok:
	xor		eax,eax
.done:
	pop		ebx
	pop		ebp
	ret		

parse_value_hex:
	push   ebp
	mov    ebp,esp
	push   edi
	push   esi
	push   ebx
	sub    esp,0x1c
	mov    ebx,dword [ebp+0x8]
	cmp    byte [ebx],0x30
	jne    .parse_56
	mov    al,byte [ebx+0x1]
	and    eax,0xffffffdf
	cmp    al,0x58
	jne    .parse_56
	add    ebx,0x2
.parse_56:
	or     eax,0xffffffff
	cmp    byte [ebx],0x0
	je     .parse_af
	mov    ecx,ebx
	xor    esi,esi
	xor    edi,edi
.parse_64:
	mov    eax,ecx
	sub    eax,ebx
	cmp    eax,dword [ebp+0xc]
	jge    .parse_a5
	cmp    byte [ecx],0x0
	je     .parse_a5
	lea    eax,[ebp-0x11]
	mov    dword [ebp-0x24],ecx
	push   eax
	push   ecx
	call    parse_nib
	mov    ecx,dword [ebp-0x24]
	pop    edx
	pop    edx
	inc    ecx
	test   eax,eax
	jne    .parse_a0
	mov    edx,edi
	mov    eax,esi
	xor    edi,edi
	shld   edx,esi,0x4
	movzx  esi,byte [ebp-0x11]
	shl    eax,0x4
	add    esi,eax
	adc    edi,edx
	jmp    .parse_64
.parse_a0:
	or     eax,0xffffffff
	jmp    .parse_af
.parse_a5:
	mov    eax,dword [ebp+0x10]
	mov    dword [eax],esi
	mov    dword [eax+0x4],edi
	xor    eax,eax
.parse_af:
	lea    esp,[ebp-0xc]
	pop    ebx
	pop    esi
	pop    edi
	pop    ebp
	ret    

parse_uint32_hex:
	push   ebp
	mov    ebp,esp
	sub    esp,0x14
	lea    eax,[ebp-0x8]
	push   eax
	push   0x8
	push   dword [ebp+0x8]
	call   parse_value_hex
	add    esp,0x10
	test   eax,eax
	jne    .parse_131
	mov    edx,dword [ebp+0xc]
	mov    ecx,dword [ebp-0x8]
	mov    dword [edx],ecx
	jmp    .parse_134
.parse_131:
	or     eax,0xffffffff
.parse_134:
	leave  
	ret    

